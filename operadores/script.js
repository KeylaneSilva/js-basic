// Operadores Aritimetricos

let salario = 100;

console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);

// + - * /

let idade = 21;
console.log(idade++);
console.log(idade--);
console.log(idade);

// Operadores de atribuição

let monitorGamer = 900;
console.log(monitorGamer += monitorGamer); //+
console.log(monitorGamer -= monitorGamer); //-

//Operadores de igualdade

console.log(1 === 1);
console.log("1" === 1);

// Operador ternario
let pontos = 100;
let tipo = pontos > 100 ? 'premium' : 'comum';
console.log(tipo);
